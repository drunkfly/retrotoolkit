/*
  4 32 4 = 40 byte every line

  24
  192
  24	240 line every screen
*/
int
out_write_scr( void )
{
  int i;

  for( i = 0; i < 192; i++ ) {
    if( fwrite( zxscr + screl_start[i], 32, 1, out ) != 1 ) return ERR_WRITE_OUT;
  }
  for( i = 0; i < 24; i++ ) {
    if( fwrite( attrs + attrb_start[i], 32, 1, out ) != 1 ) return ERR_WRITE_OUT;
  }

  printi( 2, "out_write_scr()\n" );

  return 0;
}
