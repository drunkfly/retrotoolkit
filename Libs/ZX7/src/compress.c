#include <stdio.h>
#include <stdlib.h>

#include "zx7.h"

typedef struct Ctx {
unsigned char* output_data;
size_t output_index;
size_t bit_index;
int bit_mask;
long diff;
} Ctx;

#define output_data (ctx->output_data)
#define output_index (ctx->output_index)
#define bit_index (ctx->bit_index)
#define bit_mask (ctx->bit_mask)
#define diff (ctx->diff)
#define read_bytes(n, delta) read_bytes_(ctx, n, delta)
#define write_byte(value) write_byte_(ctx, value)
#define write_bit(value) write_bit_(ctx, value)
#define write_elias_gamma(value) write_elias_gamma_(ctx, value)
#define compress zx7_compress

static void read_bytes_(Ctx* ctx, int n, long *delta) {
   diff += n;
   if (diff > *delta)
       *delta = diff;
}

static void write_byte_(Ctx* ctx, int value) {
    output_data[output_index++] = value;
    diff--;
}

static void write_bit_(Ctx* ctx, int value) {
    if (bit_mask == 0) {
        bit_mask = 128;
        bit_index = output_index;
        write_byte(0);
    }
    if (value > 0) {
        output_data[bit_index] |= bit_mask;
    }
    bit_mask >>= 1;
}

static void write_elias_gamma_(Ctx* ctx, int value) {
    int i;

    for (i = 2; i <= value; i <<= 1) {
        write_bit(0);
    }
    while ((i >>= 1) > 0) {
        write_bit(value & i);
    }
}

unsigned char *compress(Optimal *optimal, unsigned char *input_data, size_t input_size, long skip, size_t *output_size, long *delta) {
    size_t input_index;
    size_t input_prev;
    int offset1;
    int mask;
    int i;
    Ctx ctx_, *ctx = &ctx_;

    /* calculate and allocate output buffer */
    input_index = input_size-1;
    *output_size = (optimal[input_index].bits+18+7)/8;
    output_data = (unsigned char *)malloc(*output_size);
    if (!output_data) {
         /*fprintf(stderr, "Error: Insufficient memory\n");
         exit(1);*/
         return NULL;
    }

    /* initialize delta */
    diff = *output_size - input_size + skip;
    *delta = 0;

    /* un-reverse optimal sequence */
    optimal[input_index].bits = 0;
    while (input_index != skip) {
        input_prev = input_index - (optimal[input_index].len > 0 ? optimal[input_index].len : 1);
        optimal[input_prev].bits = input_index;
        input_index = input_prev;
    }

    output_index = 0;
    bit_index = 0;
    bit_mask = 0;

    /* first byte is always literal */
    write_byte(input_data[input_index]);
    read_bytes(1, delta);

    /* process remaining bytes */
    while ((input_index = optimal[input_index].bits) > 0) {
        if (optimal[input_index].len == 0) {

            /* literal indicator */
            write_bit(0);

            /* literal value */
            write_byte(input_data[input_index]);
            read_bytes(1, delta);

        } else {

            /* sequence indicator */
            write_bit(1);

            /* sequence length */
            write_elias_gamma(optimal[input_index].len-1);

            /* sequence offset */
            offset1 = optimal[input_index].offset-1;
            if (offset1 < 128) {
                write_byte(offset1);
            } else {
                offset1 -= 128;
                write_byte((offset1 & 127) | 128);
                for (mask = 1024; mask > 127; mask >>= 1) {
                    write_bit(offset1 & mask);
                }
            }
            read_bytes(optimal[input_index].len, delta);
        }
    }

    /* sequence indicator */
    write_bit(1);

    /* end marker > MAX_LEN */
    for (i = 0; i < 16; i++) {
        write_bit(0);
    }
    write_bit(1);

    return output_data;
}
